package fr.smartprod.paperdms.workflow.repository.search;

import co.elastic.clients.elasticsearch._types.query_dsl.QueryStringQuery;
import fr.smartprod.paperdms.workflow.domain.WorkflowInstance;
import fr.smartprod.paperdms.workflow.repository.WorkflowInstanceRepository;
import java.util.List;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.PageImpl;
import org.springframework.data.domain.Pageable;
import org.springframework.data.elasticsearch.client.elc.ElasticsearchTemplate;
import org.springframework.data.elasticsearch.client.elc.NativeQuery;
import org.springframework.data.elasticsearch.core.SearchHit;
import org.springframework.data.elasticsearch.core.SearchHits;
import org.springframework.data.elasticsearch.core.query.Query;
import org.springframework.data.elasticsearch.repository.ElasticsearchRepository;
import org.springframework.scheduling.annotation.Async;

/**
 * Spring Data Elasticsearch repository for the {@link WorkflowInstance} entity.
 */
public interface WorkflowInstanceSearchRepository
    extends ElasticsearchRepository<WorkflowInstance, Long>, WorkflowInstanceSearchRepositoryInternal {}

interface WorkflowInstanceSearchRepositoryInternal {
    Page<WorkflowInstance> search(String query, Pageable pageable);

    Page<WorkflowInstance> search(Query query);

    @Async
    void index(WorkflowInstance entity);

    @Async
    void deleteFromIndexById(Long id);
}

class WorkflowInstanceSearchRepositoryInternalImpl implements WorkflowInstanceSearchRepositoryInternal {

    private final ElasticsearchTemplate elasticsearchTemplate;
    private final WorkflowInstanceRepository repository;

    WorkflowInstanceSearchRepositoryInternalImpl(ElasticsearchTemplate elasticsearchTemplate, WorkflowInstanceRepository repository) {
        this.elasticsearchTemplate = elasticsearchTemplate;
        this.repository = repository;
    }

    @Override
    public Page<WorkflowInstance> search(String query, Pageable pageable) {
        NativeQuery nativeQuery = new NativeQuery(QueryStringQuery.of(qs -> qs.query(query))._toQuery());
        return search(nativeQuery.setPageable(pageable));
    }

    @Override
    public Page<WorkflowInstance> search(Query query) {
        SearchHits<WorkflowInstance> searchHits = elasticsearchTemplate.search(query, WorkflowInstance.class);
        List<WorkflowInstance> hits = searchHits.map(SearchHit::getContent).stream().toList();
        return new PageImpl<>(hits, query.getPageable(), searchHits.getTotalHits());
    }

    @Override
    public void index(WorkflowInstance entity) {
        repository.findById(entity.getId()).ifPresent(elasticsearchTemplate::save);
    }

    @Override
    public void deleteFromIndexById(Long id) {
        elasticsearchTemplate.delete(String.valueOf(id), WorkflowInstance.class);
    }
}
