package fr.smartprod.paperdms.document.repository.search;

import co.elastic.clients.elasticsearch._types.query_dsl.QueryStringQuery;
import fr.smartprod.paperdms.document.domain.TagCategory;
import fr.smartprod.paperdms.document.repository.TagCategoryRepository;
import java.util.List;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.PageImpl;
import org.springframework.data.domain.Pageable;
import org.springframework.data.elasticsearch.client.elc.ElasticsearchTemplate;
import org.springframework.data.elasticsearch.client.elc.NativeQuery;
import org.springframework.data.elasticsearch.core.SearchHit;
import org.springframework.data.elasticsearch.core.SearchHits;
import org.springframework.data.elasticsearch.core.query.Query;
import org.springframework.data.elasticsearch.repository.ElasticsearchRepository;
import org.springframework.scheduling.annotation.Async;

/**
 * Spring Data Elasticsearch repository for the {@link TagCategory} entity.
 */
public interface TagCategorySearchRepository extends ElasticsearchRepository<TagCategory, Long>, TagCategorySearchRepositoryInternal {}

interface TagCategorySearchRepositoryInternal {
    Page<TagCategory> search(String query, Pageable pageable);

    Page<TagCategory> search(Query query);

    @Async
    void index(TagCategory entity);

    @Async
    void deleteFromIndexById(Long id);
}

class TagCategorySearchRepositoryInternalImpl implements TagCategorySearchRepositoryInternal {

    private final ElasticsearchTemplate elasticsearchTemplate;
    private final TagCategoryRepository repository;

    TagCategorySearchRepositoryInternalImpl(ElasticsearchTemplate elasticsearchTemplate, TagCategoryRepository repository) {
        this.elasticsearchTemplate = elasticsearchTemplate;
        this.repository = repository;
    }

    @Override
    public Page<TagCategory> search(String query, Pageable pageable) {
        NativeQuery nativeQuery = new NativeQuery(QueryStringQuery.of(qs -> qs.query(query))._toQuery());
        return search(nativeQuery.setPageable(pageable));
    }

    @Override
    public Page<TagCategory> search(Query query) {
        SearchHits<TagCategory> searchHits = elasticsearchTemplate.search(query, TagCategory.class);
        List<TagCategory> hits = searchHits.map(SearchHit::getContent).stream().toList();
        return new PageImpl<>(hits, query.getPageable(), searchHits.getTotalHits());
    }

    @Override
    public void index(TagCategory entity) {
        repository.findById(entity.getId()).ifPresent(elasticsearchTemplate::save);
    }

    @Override
    public void deleteFromIndexById(Long id) {
        elasticsearchTemplate.delete(String.valueOf(id), TagCategory.class);
    }
}
